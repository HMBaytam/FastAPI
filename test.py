# function that takes 2 variables and returns the product of both variables
def multiply(x, y):
    return x * y

# function that takes 2 variables and returns the first variable raised to the power of the second variable
def power(x, y):
    return x ** y

# function that takes 2 variables and returns the second variable raised to the power of the first variable
def power2(x, y):
    return y ** x

